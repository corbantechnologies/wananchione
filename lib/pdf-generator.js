import { jsPDF } from "jspdf";

export const generateMigrationChecklist = (saccoName = "Wananchi One") => {
    const doc = new jsPDF();
    const primaryColor = "#D4AF37"; // Wananchi Gold
    
    // Header
    doc.setFillColor(primaryColor);
    doc.rect(0, 0, 210, 40, "F");
    
    doc.setTextColor("#FFFFFF");
    doc.setFontSize(22);
    doc.setFont("helvetica", "bold");
    doc.text(`${saccoName} Migration Checklist`, 20, 25);
    
    doc.setFontSize(10);
    doc.setFont("helvetica", "normal");
    doc.text("Your step-by-step guide to a successful system onboarding", 20, 32);

    let yPos = 55;

    const addPhase = (title, items) => {
        doc.setTextColor(primaryColor);
        doc.setFontSize(14);
        doc.setFont("helvetica", "bold");
        doc.text(title, 20, yPos);
        yPos += 10;

        doc.setTextColor("#333333");
        doc.setFontSize(11);
        doc.setFont("helvetica", "normal");
        
        items.forEach(item => {
            doc.rect(20, yPos - 4, 4, 4);
            doc.text(item, 30, yPos);
            yPos += 8;
        });
        
        yPos += 5;
    };

    addPhase("Phase 1: System Foundation", [
        "Setup General Ledger (Chart of Accounts)",
        "Configure Payment Accounts (Bank/M-Pesa)",
        "Define Fee Types (Registration, Shares, etc.)",
        "Configure Loan Products (Interest & GL Mapping)",
        "Configure Saving Products (Deposit Rules)"
    ]);

    addPhase("Phase 2: Member Migration", [
        "Clean member data in Excel (Unique Email/Phone)",
        "Download Member Migration Template",
        "Upload Member Register (CSV or Bulk Form)",
        "Verify all members have received invites"
    ]);

    addPhase("Phase 3: Savings & Fees", [
        "Download Savings/Fees Migration Template",
        "Map Member Numbers to current balances",
        "Upload Opening Savings Balances",
        "Upload Outstanding Fee Balances"
    ]);

    addPhase("Phase 4: Loan Migration", [
        "Decide between Bulk Apps (Automated) or Legacy (Manual)",
        "Prepare Loan Outstanding Balances",
        "Upload Loans and verify repayment schedules"
    ]);

    addPhase("Phase 5: Final Validation", [
        "Run the Trial Balance report",
        "Check Balance Sheet for consistency",
        "Verify 5-10 random members' statements",
        "Go Live!"
    ]);

    doc.setFontSize(9);
    doc.setTextColor("#999999");
    doc.text(`Generated by ${saccoName} Onboarding Center`, 20, 285);
    doc.text(new Date().toLocaleDateString(), 170, 285);

    doc.save(`${saccoName.replace(/\s+/g, '_')}_Migration_Checklist.pdf`);
};
